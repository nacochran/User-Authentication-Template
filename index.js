passport.use(
  "local",
  new Strategy(async function verify(username, password, cb) {
    try {
      const result = await db.query("SELECT * FROM users WHERE email = $1 ", [
        username,
      ]);
      if (result.rows.length > 0) {
        const user = result.rows[0];
        const storedHashedPassword = user.password;
        bcrypt.compare(password, storedHashedPassword, (err, valid) => {
          if (err) {
            console.error("Error comparing passwords:", err);
            return cb(err);
          } else {
            if (valid) {
              return cb(null, user);
            } else {
              return cb(null, false);
            }
          }
        });
      } else {
        return cb("User not found");
      }
    } catch (err) {
      console.log(err);
    }
  })
);

// note: the original async/await method does not work because Passport's authenticate middle
// is not designed to work directly with asynchronous operations like await
passport.use(
  "google",
  new GoogleStrategy(
    {
      clientID: process.env.GOOGLE_CLIENT_ID,
      clientSecret: process.env.GOOGLE_CLIENT_SECRET,
      callbackURL: "http://localhost:3000/auth/google/secrets",
      userProfileURL: "https://www.googleapis.com/oauth2/v3/userinfo",
    },
    (accessToken, refreshToken, profile, cb) => {
      try {
        const insertUserPromise = new Promise(async (resolve, reject) => {
          const result = await db.query("SELECT * FROM users WHERE email = $1", [profile.email]);
          if (result.rows.length === 0) {
            const newUser = await db.query("INSERT INTO users (email, password) VALUES ($1, $2) RETURNING *", [profile.email, "google"]);
            resolve(newUser.rows[0]);
          } else {
            resolve(result.rows[0]);
          }
        });

        insertUserPromise
          .then((user) => {
            console.log(user);
            return cb(null, user);
          })
          .catch((error) => {
            console.error("Error during user insertion:", error);
            return cb(error);
          });
      } catch (err) {
        return cb(err);
      }
    }
  )
);
